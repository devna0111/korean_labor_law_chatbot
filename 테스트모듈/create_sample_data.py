from pathlib import Path

def create_sample_labor_law():
    """근로기준법 샘플 데이터 생성"""
    
    sample_law = """# 근로기준법 (샘플)

## 제1장 총칙

### 제1조(목적)
이 법은 헌법에 따라 근로조건의 기준을 정함으로써 근로자의 기본적 생활을 보장, 향상시키며 균형있는 국민경제의 발전을 꾀하는 것을 목적으로 한다.

### 제2조(정의)
① 이 법에서 사용하는 용어의 뜻은 다음과 같다.
1. "근로자"란 직업의 종류와 관계없이 임금을 목적으로 사업이나 사업장에 근로를 제공하는 사람을 말한다.
2. "사용자"란 사업주 또는 사업 경영 담당자, 그 밖에 근로자에 관한 사항에 대하여 사업주를 위하여 행위하는 자를 말한다.
3. "근로"란 정신노동과 육체노동을 말한다.
4. "근로계약"이란 근로자가 사용자에게 근로를 제공하고 사용자는 이에 대하여 임금을 지급하는 것을 목적으로 체결된 계약을 말한다.

## 제2장 근로계약

### 제17조(근로조건의 명시)
① 사용자는 근로계약을 체결할 때에 근로자에게 다음 각 호의 사항을 명시하여야 한다. 근로계약 체결 후 다음 각 호의 사항을 변경하는 경우에도 또한 같다.
1. 임금
2. 소정근로시간
3. 제55조에 따른 휴일
4. 제60조에 따른 연차 유급휴가
5. 그 밖에 대통령령으로 정하는 근로조건

## 제4장 근로시간과 휴식

### 제50조(근로시간)
① 1주 간의 근로시간은 휴게시간을 제외하고 40시간을 초과할 수 없다.
② 1일의 근로시간은 휴게시간을 제외하고 8시간을 초과할 수 없다.

### 제56조(연장·야간 및 휴일 근로)
① 사용자는 연장근로(제53조·제59조 및 제69조 단서에 따라 연장된 시간의 근로)에 대하여는 통상임금의 100분의 50 이상을 가산하여 지급하여야 한다.

### 제60조(연차 유급휴가)
① 사용자는 1년간 80퍼센트 이상 출근한 근로자에게 15일의 유급휴가를 주어야 한다.

## 제5장 여성과 소년

### 제74조(임산부의 보호)
① 사용자는 임신 중의 여성에게 출산 전과 출산 후를 통하여 90일의 보호휴가를 주어야 한다. 이 경우 휴가 기간의 배정은 출산 후에 45일 이상이 되어야 한다.
"""
    
    output_path = Path("data/raw/laws/근로기준법_샘플.txt")
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    with open(output_path, "w", encoding="utf-8") as f:
        f.write(sample_law)
    
    print(f"✓ 생성 완료: {output_path}")
    return output_path


def create_sample_faq():
    """FAQ 샘플 데이터 생성"""
    
    sample_faq = """# 고용노동부 FAQ (샘플)

## Q1. 주 40시간제란 무엇인가요?

**A:** 근로기준법 제50조에 따라 1주일 근로시간은 휴게시간을 제외하고 40시간을 초과할 수 없습니다. 1일 근로시간은 8시간을 초과할 수 없습니다.

**관련 법조항:** 근로기준법 제50조

---

## Q2. 연차휴가는 어떻게 계산하나요?

**A:** 1년간 80% 이상 출근한 근로자에게 15일의 유급휴가가 부여됩니다. 
- 계속 근로연수가 1년 미만인 경우: 1개월 개근 시 1일의 유급휴가
- 1년 이상: 15일 + (계속 근로연수 - 1년) × 1일 (최대 25일)

**관련 법조항:** 근로기준법 제60조

---

## Q3. 연장근로 수당은 얼마인가요?

**A:** 연장근로, 야간근로, 휴일근로에 대해서는 통상임금의 50% 이상을 가산하여 지급해야 합니다.

예시:
- 통상시급 10,000원인 경우
- 연장근로 시급 = 10,000원 + (10,000원 × 50%) = 15,000원

**관련 법조항:** 근로기준법 제56조

---

## Q4. 출산휴가는 얼마나 받을 수 있나요?

**A:** 임신 중인 여성 근로자는 출산 전후를 통해 90일의 보호휴가를 받을 수 있습니다. 
이 중 출산 후 45일 이상은 반드시 사용해야 합니다.

**관련 법조항:** 근로기준법 제74조

---

## Q5. 근로계약서에 꼭 명시해야 할 내용은?

**A:** 근로계약 체결 시 다음 사항을 반드시 서면으로 명시해야 합니다:
1. 임금
2. 소정근로시간
3. 휴일
4. 연차 유급휴가
5. 기타 대통령령으로 정하는 근로조건

**관련 법조항:** 근로기준법 제17조
"""
    
    output_path = Path("data/raw/faqs/고용노동부FAQ_샘플.md")
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    with open(output_path, "w", encoding="utf-8") as f:
        f.write(sample_faq)
    
    print(f"✓ 생성 완료: {output_path}")
    return output_path


